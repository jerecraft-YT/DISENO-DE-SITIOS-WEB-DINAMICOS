let a = 10;
let b = 5;

let suma = a + b;
let resta = a - b;
let multiplicacion = a * b;
let division = a / b;
let modulo = a % b;
let incremento = ++a;
let decremento = --b;
let potencia = a ** 2;

console.log("Suma:", suma);
console.log("Resta:", resta);
console.log("Multiplicación:", multiplicacion);
console.log("División:", division);
console.log("Módulo:", modulo);
console.log("Incremento:", incremento);
console.log("Decremento:", decremento);
console.log("Potencia:", potencia);

// operadores relacionales
let x = 10;
let y = 5;

console.log("x es mayor que y:", x > y); // mayor que
console.log("x es menor que y:", x < y); // menor que
console.log("x es mayor o igual que y:", x >= y); // mayor o igual que
console.log("x es menor o igual que y:", x <= y); // menor o igual que
console.log("x es igual a y:", x == y); // igualdad
console.log("x es diferente de y:", x != y); // desigualdad
console.log("x es estrictamente diferente de y:", x !== y); // desigualdad estricta
console.log("x es estrictamente igual a y:", x === y); // igualdad estricta
